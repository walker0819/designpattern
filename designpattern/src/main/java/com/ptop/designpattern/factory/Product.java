package com.ptop.designpattern.factory;

public class Product {
	private String type;
	private double value;

	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
}
